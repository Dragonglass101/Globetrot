package com.example.globetrot

import android.os.Bundle
import androidx.fragment.app.Fragment
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import android.widget.Button
import android.widget.TextView
import androidx.navigation.Navigation


class Register : Fragment() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

    }

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        // Inflate the layout for this fragment
        var view = inflater.inflate(R.layout.fragment_register, container, false)
        view.findViewById<TextView>(R.id.sign_in).setOnClickListener {
            Navigation.findNavController(view).navigate(R.id.action_register_to_login)
        }
        view.findViewById<Button>(R.id.create_account_button).setOnClickListener {
            Navigation.findNavController(view).navigate(R.id.action_register_to_createProfile)
        }
        return view
    }

}